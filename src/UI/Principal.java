    public boolean validarSeleccion(char t) {
        JTable temp = null;
        String mensaje="";
        switch(t){
            case 'c':
                temp = jt_clientes;
                mensaje = "ningun cliente";
;               break;
                
            case 'l':
                temp = jt_llantas;
                mensaje = "ninguna llanta";
                break;
                
            case 'a':
                temp = jt_aros;
                mensaje = "ningun aro";
                break;
            case 'u':
                temp = jt_usuarios;
                mensaje = "ningun usuario";
                break;
            case 'f':
                temp = jt_facturas;
                mensaje = "ninguna factura";
                break;
        }
        filaSeleccionada = temp.getSelectedRow();
        if (filaSeleccionada >= 0) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "No ha seleccionado "+mensaje, "Llantas y reencauches Griegos", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
    }

    private void eliminarCliente() {
        BL_Cliente blCliente;
        if (validarSeleccion('c')) {
            UI_Cliente uic = new UI_Cliente(this, rootPaneCheckingEnabled);
            int fila = jt_clientes.getSelectedRow();
            int numeroFila = Integer.parseInt("" + jt_clientes.getValueAt(fila, 0));
            blCliente = listaClientes.get(numeroFila);
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Desea eliminar a " + blCliente.getNombre() + "?", "Alerta", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                if (blCliente.eliminarCliente()) {
                    JOptionPane.showMessageDialog(null, "Cliente Elimiado con exito", "Llantas y reencauches Griegos", JOptionPane.INFORMATION_MESSAGE);
                    cargarClientes();
                } else {
                    JOptionPane.showMessageDialog(null, "Fallo al eliminar el cliente", "Llantas y reencauches Griegos", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }
    

